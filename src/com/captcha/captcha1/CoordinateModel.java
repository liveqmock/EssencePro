package com.captcha.captcha1;

public class CoordinateModel {

    private Double x;
    private Double y;

    public CoordinateModel(double x1, double y1) {
        this.x = x1;
        this.y = y1;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

}
